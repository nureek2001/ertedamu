const getTaskFromPool = (pool: any[], monthIndex: number, itemsPerMonth: number = 1) => {
  // Мы "растягиваем" пул задач на 72 месяца (6 лет).
  // pool.length задач нужно распределить на 72 месяца.
  // Вычисляем прогресс:
  const totalMonths = 72;
  const poolSize = pool.length;
  
  // Индекс задачи в массиве на основе текущего месяца
  // Например: Месяц 1 (idx 0) -> задача 0. Месяц 36 (idx 35) -> задача ~середина.
  const rawIndex = Math.floor((monthIndex / totalMonths) * poolSize);
  const safeIndex = Math.min(Math.max(0, rawIndex), poolSize - 1);
  
  return pool[safeIndex];
};

const buildLibrary = (): ActivityItem[] => {
  const library: ActivityItem[] = [];
  const categories: ActivityCategory[] = [
    'fine_motor', 'cognitive', 'speech', 'self_care', 'social_emotional', 'gross_motor'
  ];

  // Маппинг категорий на базы данных
  const POOLS: Record<ActivityCategory, any[]> = {
    fine_motor: DB_FINE_MOTOR,
    cognitive: DB_COGNITIVE,
    speech: DB_SPEECH,
    self_care: DB_SELF_CARE,
    social_emotional: DB_SOCIAL,
    gross_motor: DB_GROSS_MOTOR,
  };

  // 1. Генерируем 36 месяцев (Младенцы и Тоддлеры)
  for (let m = 1; m <= 36; m++) {
    const ageLabel = `${m} мес.`;
    
    categories.forEach(cat => {
      const pool = POOLS[cat];
      
      // Чтобы получить 3 разные активности для одного месяца, 
      // мы берем "текущую" задачу из пула и две соседние (или вариации)
      
      // Базовый индекс для этого месяца
      // Ускоряем прогресс для первых 3 лет, так как там развитие быстрее (pool задач в основном покрывает первые годы)
      const poolSize = pool.length;
      // Логарифмическое или линейное распределение. 
      // Допустим, к 36 месяцу мы должны показать примерно 70% базы.
      const rawIndex = Math.floor(((m - 1) / 48) * poolSize); 
      
      // Генерируем 3 вариации
      for (let i = 0; i < 3; i++) {
        // Смещение индекса, чтобы задачи были разными
        // i=0 -> задача N
        // i=1 -> задача N+1 (чуть сложнее)
        // i=2 -> задача N (но с другим подходом)
        let index = rawIndex + (i === 1 ? 1 : 0);
        if (index >= poolSize) index = poolSize - 1;
        
        const task = pool[index];
        const meta = CATEGORY_META[cat];

        // Создаем уникальные заголовки
        let title = task.t;
        let subtitle = task.obj;
        let difficulty: 'легко' | 'средне' | 'сложно' = 'средне';

        if (i === 0) {
            difficulty = 'легко';
            title = `${task.t} (Старт)`;
        } else if (i === 1) {
            difficulty = 'сложно';
            title = `${task.t} (Профи)`;
            subtitle = `Усложнение: ${task.obj}`;
        } else {
            difficulty = 'средне';
            title = `${task.t} (Игра)`;
            subtitle = `Веселый способ: ${task.obj}`;
        }

        library.push({
          id: `m${m}-${cat}-${i}`,
          category: cat,
          title: title,
          subtitle: subtitle,
          minMonths: m,
          maxMonths: m, // Строго этот месяц
          minutes: 3 + i * 2 + Math.floor(m/6), // Время растет с возрастом
          difficulty: difficulty,
          objective: `Развить навык: ${task.obj}. Возраст: ${ageLabel}.`,
          materials: [...task.mat, i === 2 ? 'Любимая игрушка' : ''],
          steps: [
            'Подготовьте безопасное пространство и материалы.',
            `Основное задание: ${task.t}. Покажите ребенку, как это делается.`,
            i === 1 ? 'Попробуйте сделать это чуть быстрее или другой рукой.' : 'Дайте ребенку время исследовать предмет самостоятельно.',
            'Похвалите за любые попытки и успехи!'
          ].filter(Boolean),
          skills: [meta.shortLabel, task.obj.split(' ')[0], 'Внимание'],
          milestone: i === 1 ? `Освоение навыка: ${task.t}` : undefined,
          videoUrl: "https://storage.googleapis.com/my-app-videos-01/videomy.mp4"
        });
      }
    });
  }

  // 2. Генерируем Года (4, 5, 6 лет)
  // Для них берем задачи из конца списков
  [4, 5, 6].forEach(year => {
    const startM = year * 12;
    const endM = startM + 11;
    
    categories.forEach(cat => {
      const pool = POOLS[cat];
      // Берем задачи из последней трети списка
      const baseIndex = Math.floor(pool.length * 0.7) + (year - 4) * 3;
      
      for (let i = 0; i < 3; i++) {
        let index = baseIndex + i;
        if (index >= pool.length) index = pool.length - 1;
        const task = pool[index];
        const meta = CATEGORY_META[cat];

        library.push({
          id: `y${year}-${cat}-${i}`,
          category: cat,
          title: `${task.t} (${year} лет)`,
          subtitle: `Уровень для ${year} лет: ${task.obj}`,
          minMonths: startM,
          maxMonths: endM,
          minutes: 10 + i * 5,
          difficulty: i === 0 ? 'легко' : i === 1 ? 'средне' : 'сложно',
          objective: task.obj,
          materials: task.mat,
          steps: [
            `Объясните правила игры "${task.t}".`,
            'Выполняйте задание вместе, затем дайте самостоятельность.',
            'Обсудите результат: что получилось, а что было сложно.'
          ],
          skills: [meta.shortLabel, 'Самостоятельность'],
          milestone: `Навык ${year} лет: ${task.t}`
        });
      }
    });
  });

  return library;
};

export const ACTIVITY_LIBRARY = buildLibrary();

